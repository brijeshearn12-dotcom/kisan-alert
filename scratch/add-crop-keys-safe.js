for (const lang of Object.keys(additions)) {
  const dictionary = additions[lang];
  let dictStr = '';
  for (const [k, v] of Object.entries(dictionary)) {
    const escapedVal = v.replace(/'/g, "\\'");
    // We add a leading comma before key since it follows the last element
    dictStr += `,\n  '${k}': '${escapedVal}'`;
  }

  const searchPattern = targets[lang];
  const insertionPoint = content.indexOf(searchPattern);
  if (insertionPoint !== -1) {
    const idx = insertionPoint + searchPattern.length;
    content = content.slice(0, idx) + dictStr + content.slice(idx);
    console.log(`Appended ${lang} crop keys`);
  } else {
    console.error(`Could not find target pattern for ${lang}`);
  }
}

fs.writeFileSync(filePath, content, 'utf8');
console.log('SUCCESS: Translations file updated with crop keys!');
